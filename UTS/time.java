    @Override
    public double convert(double input, String from, String to) {
        double faktorFrom = 1;
        double faktorTo = 1;

        if (from.equals("Second")) faktorFrom = 1;
        else if (from.equals("Minute")) faktorFrom = 60;
        else if (from.equals("Hour")) faktorFrom = 3600;
        else if (from.equals("Day")) faktorFrom = 86400;

        if (to.equals("Second")) faktorTo = 1;
        else if (to.equals("Minute")) faktorTo = 60;
        else if (to.equals("Hour")) faktorTo = 3600;
        else if (to.equals("Day")) faktorTo = 86400;

        return (input * faktorFrom) / faktorTo;
}

