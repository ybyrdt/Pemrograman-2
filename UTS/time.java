    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertActionPerformed
        // TODO add your handling code here:
       if (cmbFrom.getSelectedIndex() == 0 || cmbTo.getSelectedIndex() == 0) {
        JOptionPane.showMessageDialog(this,
            "Pilih satuan waktu terlebih dahulu!",
            "Peringatan",
            JOptionPane.WARNING_MESSAGE);
        return;
        }
       
       double input;
       try{
           input = Double.parseDouble(txtInput.getText());
       } catch(NumberFormatException e) {
           JOptionPane.showMessageDialog(this,
                   "Masukkan angka yang valid!",
                   "Error",
                   JOptionPane.ERROR_MESSAGE);
           return;
       }
       
       String from = cmbFrom.getSelectedItem().toString();
       String to = cmbTo.getSelectedItem().toString();
       
       double result = convert (input, from, to);
       
       if (Math.abs(result - Math.round(result)) < 1e-9) {
       txtResult.setText(String.valueOf((long) Math.round(result)));
       } else {
        DecimalFormat df = new DecimalFormat("#.########");
        txtResult.setText(df.format(result));
       }
    }//GEN-LAST:event_btnConvertActionPerformed

